ax2.set_title('Top 5 & Bottom 5 Districts by Revenue Utilization', fontweight='bold', fontsize=12)
ax2.axvline(overall_utilization, color='red', linestyle='--', linewidth=2, label=f'Overall Avg: {overall_utilization:.1f}%')
ax2.legend()
for i, v in enumerate(top_bottom_util['Utilization_Rate_%']):
    ax2.text(v + 0.5, i, f'{v:.1f}%', va='center')

# Chart 3: Revenue Allocation vs Utilization by District
print("3. Generating: Top 10 Districts - Allocated vs Utilized Revenue...")
ax3 = fig.add_subplot(gs[1, 0])
df_sorted = df.sort_values('Revenue_Allocated_Lakhs', ascending=False).head(10)
