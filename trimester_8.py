# Load data from CSV string
df = pd.read_csv(StringIO(csv_data))

print("\n✓ Data loaded successfully!")
print(f"✓ Districts analyzed: {len(df)}")
print(f"\nFirst few rows:\n{df.head()}")

# ============================================================================
# STEP 2: CALCULATE SUMMARY STATISTICS
# ============================================================================

print("\n" + "=" * 80)
print("SUMMARY STATISTICS")
print("=" * 80)

total_allocated = df['Revenue_Allocated_Lakhs'].sum()
total_utilized = df['Revenue_Utilized_Lakhs'].sum()
total_returned = df['Revenue_Returned_Lakhs'].sum()
overall_utilization = (total_utilized / total_allocated * 100)

total_planned = df['Works_Planned'].sum()
total_completed = df['Works_Completed'].sum()
total_pending = df['Works_Pending'].sum()
overall_completion = (total_completed / total_planned * 100)

print(f"\nREVENUE METRICS:")
print(f"  Total Allocated: ₹{total_allocated:,.0f} Lakhs")
print(f"  Total Utilized: ₹{total_utilized:,.0f} Lakhs")
print(f"  Total Returned: ₹{total_returned:,.0f} Lakhs")
print(f"  Overall Utilization: {overall_utilization:.2f}%")

print(f"\nWORK COMPLETION METRICS:")
print(f"  Total Planned: {total_planned} works")
print(f"  Total Completed: {total_completed} works")
print(f"  Total Pending: {total_pending} works")
print(f"  Overall Completion: {overall_completion:.2f}%")

# Top and Bottom performers
print(f"\nTOP 5 PERFORMERS (by Completion Rate):")
top_5 = df.nlargest(5, 'Completion_Rate_%')
for idx, (_, row) in enumerate(top_5.iterrows(), 1):
    print(f"  {idx}. {row['District']:<20} → {row['Completion_Rate_%']:.2f}%")

print(f"\nBOTTOM 5 PERFORMERS (by Completion Rate):")
bottom_5 = df.nsmallest(5, 'Completion_Rate_%')
for idx, (_, row) in enumerate(bottom_5.iterrows(), 1):
    print(f"  {idx}. {row['District']:<20} → {row['Completion_Rate_%']:.2f}%")

# Correlation analysis
correlation = df['Completion_Rate_%'].corr(df['Utilization_Rate_%'])
print(f"\nCORRELATION ANALYSIS:")
print(f"  Pearson Correlation (Completion vs Utilization): {correlation:.3f}")
print(f"  Interpretation: Weak to moderate positive correlation")

