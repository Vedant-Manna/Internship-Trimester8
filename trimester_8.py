colors = ['#2ecc71']*5 + ['#e74c3c']*5
bars = ax1.barh(top_bottom_comp['District'], top_bottom_comp['Completion_Rate_%'], color=colors)
ax1.set_xlabel('Completion Rate (%)', fontweight='bold')
ax1.set_title('Top 5 & Bottom 5 Districts by Work Completion Rate', fontweight='bold', fontsize=12)
ax1.axvline(overall_completion, color='blue', linestyle='--', linewidth=2, label=f'Overall Avg: {overall_completion:.1f}%')
ax1.legend()
for i, v in enumerate(top_bottom_comp['Completion_Rate_%']):
    ax1.text(v + 0.5, i, f'{v:.1f}%', va='center')

# Chart 2: Top vs Bottom Districts - Revenue Utilization
print("2. Generating: Top 5 & Bottom 5 Districts by Revenue Utilization...")
